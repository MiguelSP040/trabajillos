public class Operaciones {
    public int sumaNumero(int a, int b){
        return a + b;
    }

    public int restaNumero(int a, int b){
        return a - b;
    }

    public int multiplicacionNumero(int a, int b){
        return a * b;
    }

    public int dividirNumero(int a, int b){
        return a / b;
    }
}
